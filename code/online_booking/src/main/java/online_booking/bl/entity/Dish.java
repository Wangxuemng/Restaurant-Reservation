package online_booking.bl.entity;

public class Dish {
    private String name;
    private String img;

    public Dish() {
    }

    public Dish(String name, String img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
